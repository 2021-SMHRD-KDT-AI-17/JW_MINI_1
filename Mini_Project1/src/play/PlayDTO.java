package play;

public class PlayDTO {

	private int money;
	private int hp;
	private int cnt_date;
	private String id;
	private int ran;
	private int cnt;
	
	
	public int getRan() {
		return ran;
	}
	public void setRan(int ran) {
		this.ran = ran;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	
	
	
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getCnt_date() {
		return cnt_date;
	}
	public void setCnt_date(int cnt_date) {
		this.cnt_date = cnt_date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
