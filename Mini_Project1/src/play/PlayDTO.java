package play;

public class PlayDTO {

	private int money;
	private int hp;
	private int cnt_date;
	private String id;
	private int sum_opp;
	private int work_opp;
	
	
	public int getSum_opp() {
		return sum_opp;
	}
	public void setSum_opp(int sum_opp) {
		this.sum_opp = sum_opp;
	}
	public int getWork_opp() {
		return work_opp;
	}
	public void setWork_opp(int work_opp) {
		this.work_opp = work_opp;
	}
	
	

	
	
	public int getMoney() {
		return money;
	}
	public void setMoney(int money) {
		this.money = money;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public int getCnt_date() {
		return cnt_date;
	}
	public void setCnt_date(int cnt_date) {
		this.cnt_date = cnt_date;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
